////////////////////////////////////////////////////////////////////////////////
/// @brief V8 context
////////////////////////////////////////////////////////////////////////////////

        v8::Persistent<v8::Context> _context;

////////////////////////////////////////////////////////////////////////////////
/// @brief action queue
////////////////////////////////////////////////////////////////////////////////

        std::string _actionQueue;

////////////////////////////////////////////////////////////////////////////////
/// @brief allowed action contexts
////////////////////////////////////////////////////////////////////////////////

        std::set<std::string> _allowedContexts;

////////////////////////////////////////////////////////////////////////////////
/// @brief modules path
////////////////////////////////////////////////////////////////////////////////

        string _startupModules;

////////////////////////////////////////////////////////////////////////////////
/// @brief startup path
