#ifndef ARANGODB_MVCC_TRANSACTION_COLLECTION_H
#define ARANGODB_MVCC_TRANSACTION_COLLECTION_H 1

#include "Basics/Common.h"
#include "VocBase/collection.h"
#include "VocBase/voc-types.h"
#include "VocBase/vocbase.h"

struct TRI_barrier_s;
struct TRI_document_collection_t;
struct TRI_shaper_s;
struct TRI_vocbase_s;
struct TRI_vocbase_col_s;

namespace triagens {
  namespace mvcc {

    class MasterpointerManager;
    class Transaction;

// -----------------------------------------------------------------------------
// --SECTION--                                       class TransactionCollection
// -----------------------------------------------------------------------------

    class TransactionCollection {

      friend struct CollectionOperations;

      enum class LockType {
        UNLOCKED,
        LOCKED_READ,
        LOCKED_WRITE
      };

// -----------------------------------------------------------------------------
// --SECTION--                                        constructors / destructors
// -----------------------------------------------------------------------------

      public:

////////////////////////////////////////////////////////////////////////////////
/// @brief create the collection, using a collection id
////////////////////////////////////////////////////////////////////////////////

        TransactionCollection (struct TRI_vocbase_s*,
                               TRI_voc_cid_t);

////////////////////////////////////////////////////////////////////////////////
/// @brief create the collection, using a collection name
////////////////////////////////////////////////////////////////////////////////

        TransactionCollection (struct TRI_vocbase_s*,
                               std::string const&);

////////////////////////////////////////////////////////////////////////////////
/// @brief create the collection, using a collection name
////////////////////////////////////////////////////////////////////////////////

        TransactionCollection (struct TRI_vocbase_s*,
                               char const*);

////////////////////////////////////////////////////////////////////////////////
/// @brief destroy the collection
////////////////////////////////////////////////////////////////////////////////

        ~TransactionCollection ();

// -----------------------------------------------------------------------------
// --SECTION--                                                    public methods
// -----------------------------------------------------------------------------

      public:

////////////////////////////////////////////////////////////////////////////////
/// @brief return the collection id
////////////////////////////////////////////////////////////////////////////////

        inline TRI_voc_cid_t id () const {
          return _collection->_cid;
        }

////////////////////////////////////////////////////////////////////////////////
/// @brief return the collection name
////////////////////////////////////////////////////////////////////////////////

        inline std::string name () const {
          return std::string(_collection->_name);
        }

////////////////////////////////////////////////////////////////////////////////
/// @brief return the underlying database
////////////////////////////////////////////////////////////////////////////////

        inline struct TRI_vocbase_s* vocbase () const {
          return _collection->_vocbase;
        }

////////////////////////////////////////////////////////////////////////////////
/// @brief return the underlying collection 
////////////////////////////////////////////////////////////////////////////////

        inline struct TRI_vocbase_col_s* collection () const {
          return _collection;
        }

////////////////////////////////////////////////////////////////////////////////
/// @brief return the underlying collection 
////////////////////////////////////////////////////////////////////////////////

        inline struct TRI_document_collection_t* documentCollection () const {
          return _collection->_collection;
        }

////////////////////////////////////////////////////////////////////////////////
/// @brief return the collection's lock status
////////////////////////////////////////////////////////////////////////////////
  
        inline bool isLocked () const {
          return _locked == TransactionCollection::LockType::UNLOCKED;
        }

////////////////////////////////////////////////////////////////////////////////
/// @brief read-lock the collection
////////////////////////////////////////////////////////////////////////////////

        void lockRead ();

////////////////////////////////////////////////////////////////////////////////
/// @brief read-lock the collection, using a timeout (in microseconds)
////////////////////////////////////////////////////////////////////////////////

        void lockRead (uint64_t);

////////////////////////////////////////////////////////////////////////////////
/// @brief write-lock the collection
////////////////////////////////////////////////////////////////////////////////

        void lockWrite ();

////////////////////////////////////////////////////////////////////////////////
/// @brief write-lock the collection, using a timeout (in microseconds)
////////////////////////////////////////////////////////////////////////////////

        void lockWrite (uint64_t);

////////////////////////////////////////////////////////////////////////////////
/// @brief unlock the collection
////////////////////////////////////////////////////////////////////////////////

        void unlock ();

////////////////////////////////////////////////////////////////////////////////
/// @brief whether or not the collection is volatile
////////////////////////////////////////////////////////////////////////////////
  
        bool isVolatile () const;

////////////////////////////////////////////////////////////////////////////////
/// @brief return the underlying collection type
////////////////////////////////////////////////////////////////////////////////

        TRI_col_type_e type () const;

////////////////////////////////////////////////////////////////////////////////
/// @brief return the underlying collection type
////////////////////////////////////////////////////////////////////////////////

        bool isEdgeCollection () const;

////////////////////////////////////////////////////////////////////////////////
/// @brief return the collection's plan id
////////////////////////////////////////////////////////////////////////////////

        TRI_voc_cid_t planId () const;

////////////////////////////////////////////////////////////////////////////////
/// @brief return the collection's db name
////////////////////////////////////////////////////////////////////////////////

        char const* dbName () const;

////////////////////////////////////////////////////////////////////////////////
/// @brief return the collection's waitForSync flag
////////////////////////////////////////////////////////////////////////////////

        bool waitForSync () const; 

////////////////////////////////////////////////////////////////////////////////
/// @brief return the collection shaper
////////////////////////////////////////////////////////////////////////////////

        struct TRI_shaper_s* shaper () const;

////////////////////////////////////////////////////////////////////////////////
/// @brief return the collection's masterpointer manager
////////////////////////////////////////////////////////////////////////////////

        MasterpointerManager* masterpointerManager ();

////////////////////////////////////////////////////////////////////////////////
/// @brief update the document counter of the collection
////////////////////////////////////////////////////////////////////////////////

        void updateDocumentCount (int64_t);

////////////////////////////////////////////////////////////////////////////////
/// @brief return the number of documents in the collection
////////////////////////////////////////////////////////////////////////////////

        int64_t documentCount ();

////////////////////////////////////////////////////////////////////////////////
/// @brief update the document size of the collection
////////////////////////////////////////////////////////////////////////////////

        void updateDocumentSize (int64_t);

////////////////////////////////////////////////////////////////////////////////
/// @brief return the size of documents in the collection
////////////////////////////////////////////////////////////////////////////////

        int64_t documentSize ();

////////////////////////////////////////////////////////////////////////////////
/// @brief update the collection statistics
////////////////////////////////////////////////////////////////////////////////

        void updateDocumentStats (int64_t, 
                                  int64_t);

////////////////////////////////////////////////////////////////////////////////
/// @brief return the last revision id in the collection
////////////////////////////////////////////////////////////////////////////////
        
        TRI_voc_rid_t revisionId ();

////////////////////////////////////////////////////////////////////////////////
/// @brief update the last revision id of the collection
////////////////////////////////////////////////////////////////////////////////

        void updateRevisionId (TRI_voc_rid_t);

////////////////////////////////////////////////////////////////////////////////
/// @brief generate a key
////////////////////////////////////////////////////////////////////////////////

        std::string generateKey (TRI_voc_tick_t);

////////////////////////////////////////////////////////////////////////////////
/// @brief validate a key
/// this will throw if the key is invalid
////////////////////////////////////////////////////////////////////////////////

        void validateKey (std::string const&) const;

////////////////////////////////////////////////////////////////////////////////
/// @brief order a barrier for the collection
////////////////////////////////////////////////////////////////////////////////

        struct TRI_barrier_s* orderBarrier ();

////////////////////////////////////////////////////////////////////////////////
/// @brief free an unused barrier if one exists
////////////////////////////////////////////////////////////////////////////////

        void freeBarrier ();

////////////////////////////////////////////////////////////////////////////////
/// @brief run pre-commit actions
////////////////////////////////////////////////////////////////////////////////

        void preCommit (Transaction*);

////////////////////////////////////////////////////////////////////////////////
/// @brief get a string representation of the collection
////////////////////////////////////////////////////////////////////////////////

        std::string toString () const;

// -----------------------------------------------------------------------------
// --SECTION--                                                   private methods
// -----------------------------------------------------------------------------

      private:

// -----------------------------------------------------------------------------
// --SECTION--                                          non-class friend methods
// -----------------------------------------------------------------------------

      public:

////////////////////////////////////////////////////////////////////////////////
/// @brief append the transaction to an output stream
////////////////////////////////////////////////////////////////////////////////
    
        friend std::ostream& operator<< (std::ostream&, TransactionCollection const*);

        friend std::ostream& operator<< (std::ostream&, TransactionCollection const&);

// -----------------------------------------------------------------------------
// --SECTION--                                                 private variables
// -----------------------------------------------------------------------------

      private:

////////////////////////////////////////////////////////////////////////////////
/// @brief pointer to the vocbase
////////////////////////////////////////////////////////////////////////////////

        struct TRI_vocbase_s* _vocbase; 

////////////////////////////////////////////////////////////////////////////////
/// @brief pointer to the vocbase collection
////////////////////////////////////////////////////////////////////////////////

        struct TRI_vocbase_col_s* _collection; 
      
////////////////////////////////////////////////////////////////////////////////
/// @brief an optional barrier acquired for the collection
////////////////////////////////////////////////////////////////////////////////
  
        struct TRI_barrier_s* _barrier;

////////////////////////////////////////////////////////////////////////////////
/// @brief whether or not the collection is locked
////////////////////////////////////////////////////////////////////////////////

        LockType _locked;

////////////////////////////////////////////////////////////////////////////////
/// @brief sleep time when waiting to acquire a lock (in microseconds)
////////////////////////////////////////////////////////////////////////////////

        static uint64_t const LockWaitSleepTime;

    };
  }
}

#endif

