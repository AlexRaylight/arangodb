////////////////////////////////////////////////////////////////////////////////
/// @addtogroup V8Shell
/// @{
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
/// @brief string representation of an object
////////////////////////////////////////////////////////////////////////////////

function PRINT_OBJECT (object) {
  var sep = " ";

  output("{");

  for (var k in object) {
    if (object.hasOwnProperty(k)) {
      var val = object[k];

      output(sep, k, " : ");
      PRINT(val);
      sep = ", ";
    }
  }

  output(" }");
}

