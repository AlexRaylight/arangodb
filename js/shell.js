/// @{
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
/// @brief JSON representation of an array
////////////////////////////////////////////////////////////////////////////////

Array.prototype.PRINT = function() {
  if (this.length == 0) {
    output("[ ]");
  }
  else {
    var sep = " ";

    output("[");

    for (var i = 0;  i < this.length;  i++) {
      output(sep);
      PRINT(this[i]);
      sep = ", ";
    }

    output(" ]");
  }
}

