////////////////////////////////////////////////////////////////////////////////
/// @brief prints a shaped json
////////////////////////////////////////////////////////////////////////////////

  ShapedJson.prototype._PRINT = function(seen, path, names, level) {
    if (this instanceof ShapedJson) {
      internal.printObject(this, seen, path, names, level);
    }
    else {
      internal.output(this.toString());
    }
  };

